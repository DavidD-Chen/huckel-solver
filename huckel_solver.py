# Returns list of eigenvalues for any square matrix
def get_evals(A: np.ndarray):
    evals, evects = np.linalg.eig(A)
    # Rounding evals to avoid numerical instability
    evals_rounded = [round(eval, 5) for eval in evals]
    return evals_rounded

# Returns n x n Huckel matrix for a linear n polyene, where n >= 2
def get_H_linear(n: int):
    assert(n >= 2)
    H = np.zeros((n, n))
    # Hardcoding the first and last rows
    H[0, 1] = -1
    H[n-1, n-2] = -1
    if n != 2:
      i = 1
      while i < n-1:
        H[i, i-1] = -1
        H[i, i+1] = -1
        i += 1
    return H

# Returns n x n Huckel matrix for a cyclic n polyene, where n >= 3
def get_H_cyclic(n : int):
    assert(n >= 3)
    H = np.zeros((n, n))
    # Hardcoding the first and last rows
    H[0, 1], H[0, n-1] = -1, -1
    H[n-1, 0], H[n-1, n-2] = -1, -1
    i = 1
    while i < n-1:
      H[i, i-1] = -1
      H[i, i+1] = -1
      i += 1
    return H

